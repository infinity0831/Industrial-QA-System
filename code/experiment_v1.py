def calculate_score(query, passage):
    instruction = "为这个句子生成表示以用于检索相关文章："
    q_emb = model.encode(instruction + query, normalize_embeddings=True)
    p_emb = model.encode(passage, normalize_embeddings=True)
    return (q_emb @ p_emb.T) * 100

